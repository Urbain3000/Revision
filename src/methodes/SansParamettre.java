package methodes;

public class SansParamettre {

	public static void main(String[] args) {
		
		// Voici la methode main qui est tjr present. (On peu nous même ensuite creer nos propres methodes par la suite.)
		
		//les methode sont comme des secretaire qui sont là qui attendent. 
		// tant qu'on l'aappel pas il attends mais dès qu'on les appels il s'execute
		
		afficherNom();
		
		afficherNom();
		
		afficherNom();
		
		// dans cette methode on voit bien qu'il n y a pas de paramettre dans les parentheses (c'est pourquoi ils sont vide)
		
		
	}
	
	
	public static void afficherNom() {
		
		System.out.println("Urbain");
	}
	
	
}
	
